char* itoa(int value, char* result, int base) {
                // check that the base if valid
                if (base < 2 || base > 36) { *result = '\0'; return result; }

                char* ptr = result, *ptr1 = result, tmp_char;
                int tmp_value;

                do {
                        tmp_value = value;
                        value /= base;
                        *ptr++ = "zyxwvutsrqponmlkjihgfedcba9876543210123456789abcdefghijklmnopqrstuvwxyz" [35 + (tmp_value - value * base)];
                } while ( value );

                // Apply negative sign
                if (tmp_value < 0) *ptr++ = '-';
                *ptr-- = '\0';
                while(ptr1 < ptr) {
                        tmp_char = *ptr;
                        *ptr--= *ptr1;
                        *ptr1++ = tmp_char;
                }
                return result;
 }

uint8_t CapDemo(uint8_t key)
{
    uint16_t deltaCount[5];
    uint16_t i=0;
    uint16_t time = 0;


    while(time<0x50){
        TI_CAPT_Init_Baseline(&slider);
        TI_CAPT_Custom(&slider, deltaCount);
        if(deltaCount[4]>2){
            i++;
        }
        time++;
    }
    if(i>5){
        Board_ledToggle(LED8);
        switch(key){
         case 0: Dogs102x6_stringDraw(6, 34, "$", DOGS102x6_DRAW_NORMAL);
                 break;
         case 1: Dogs102x6_stringDraw(6, 42, "$", DOGS102x6_DRAW_NORMAL);
                 break;
         case 2: Dogs102x6_stringDraw(6, 50, "$", DOGS102x6_DRAW_NORMAL);
                 break;
         case 3: Dogs102x6_stringDraw(6, 60, "$", DOGS102x6_DRAW_NORMAL);
                 break;
        }
        return 1;
    }
    else{
        return 0;
    }

}

